package gt.org.isis.api.utils;

/**
 *
 * @author eliud
 */
public class FieldDto {

    private String name;
    private String title;

    public FieldDto(String name, String title) {
        this.name = name;
        this.title = title;
    }

    public String getName() {
        return name;
    }

    public String getTitle() {
        return title;
    }

}
